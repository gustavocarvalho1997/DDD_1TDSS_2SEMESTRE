package br.com.fiap.exercicio1.entities;

public class Department {
	// Atributos
	private String name;
	// Construtores
	public Department() {
		super();
	}
	public Department(String name) {
		this.name = name;
	}
	// Getters
	public String getName() {
		return name;
	}
	// Setters
	public void setName(String name) {
		this.name = name;
	}
}//CLASS